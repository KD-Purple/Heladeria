    public void LlenarLista() {
        String id = Txt1.getText();
        int i = Integer.parseInt(id);
        if (!controller.buscar2(id)) {
            String nombre = Txt2.getText();
            String precio = Txt3.getText();
            int p = Integer.parseInt(precio);
            controller.Agregar(i, nombre, p);
            Box2.addItem(nombre);
            JOptionPane.showMessageDialog(null, "Producto registrado");
            Txt1.setText("");
            Txt2.setText("");
            Txt3.setText("");
        } else {
            JOptionPane.showMessageDialog(this, "ID ya existente", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    public void LlenarTabla() {
        for (int x = 0; x < controller.lista.size(); x++) {
            Object[] datos = new Object[modelo.getColumnCount()];
            datos[0] = controller.lista.get(x).getID();
            datos[1] = controller.lista.get(x).getNombre();
            datos[2] = controller.lista.get(x).getPrecio();
            modelo.addRow(datos);
        }
    }

