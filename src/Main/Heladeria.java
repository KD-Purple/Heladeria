    private void BtnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnBorrarActionPerformed
        BtnBorrar.setEnabled(false);
        int c = 0;
        String n = TxtB.getText();
        if (n.equals("")) {
            JOptionPane.showMessageDialog(this, "Talvez quito el ID", "Error", JOptionPane.WARNING_MESSAGE);
            AreaB.setText("");
        } else {
            int r = JOptionPane.showConfirmDialog(null, "¿Seguro que lo quiere borrar?");
            if (r == JOptionPane.YES_OPTION) {
                 int num = Integer.parseInt(n);
                 for (Producto p : controller.lista) {                    
                    if (num == p.getID()) {                                             
                        break;
                    }
                    c++;
                }
                controller.insercionDirecta();
                p = controller.busquedaBinariaID(num, 0, controller.lista.size() - 1);
               if(p!=null){
                   String m = p.getNombre();
                   Box2.removeItem(m);
                        AreaB.setText("");
                        TxtB.setText("");
                        JOptionPane.showMessageDialog(null, "El producto fue eliminado");                       
               }
                
                modelo.removeRow(c);
            } else {
                if (r == JOptionPane.NO_OPTION) {
                    AreaB.setText("");
                    TxtB.setText("");
                    JOptionPane.showMessageDialog(null, "Bueno, sera en otro momento");
                }
            }
        }
    }//GEN-LAST:event_BtnBorrarActionPerformed

